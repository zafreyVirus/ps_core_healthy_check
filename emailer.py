import smtplib
import os
import matplotlib.pyplot as plt
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText
from email.mime.image import MIMEImage
from email.mime.base import MIMEBase
from email import encoders
from datetime import datetime


class EmailReport:

    def __init__(self, sender_email, sender_password):
        self.sender_email = sender_email
        self.sender_password = sender_password

    # Generate Multiple KPI Charts
    def generate_charts(self, processor):
        
        kpis = [
    "PGW-U 2/3G Gi traffic in MB (MB)",
    "4G Data traffic VDGW(CLOUD) (MB)",
    "PGW-U 2/3G Gn peak throughput in MB/s (MB/s)",
    "User Plane SGi downlink user traffic peak throughput in MB/s (MB/s) (MB/s)"
]

        chart_files = []

        for i, kpi in enumerate(kpis, start=1):
            
            if kpi not in processor.df.columns:
                print(f"KPI not found in dataset: {kpi}")
                continue

            pivot_data = processor.pivot_kpi(kpi)

            plt.figure(figsize=(10, 5))

            for column in pivot_data.columns:
                plt.plot(pivot_data.index, pivot_data[column], label=column)

            plt.xlabel("Time")
            plt.ylabel(kpi)
            plt.title(kpi)
            plt.legend()
            plt.xticks(rotation=45)
            plt.tight_layout()

            chart_path = f"chart_{i}.png"
            plt.savefig(chart_path)
            plt.close()

            chart_files.append(chart_path)

        return chart_files

    # Generate HTML Report
    def generate_html(self, health_report):

        current_time = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

        rows = ""

        for ne, data in health_report.items():

            status = data["Health Status"]

            if status == "HEALTHY":
                color = "#28a745"
            elif status == "WARNING":
                color = "#ffc107"
            else:
                color = "#dc3545"

            rows += f"""
            <tr>
                <td>{ne}</td>
                <td>{data['Peak Traffic (MB)']}</td>
                <td>{data['Peak Time']}</td>
                <td>{data['Utilization %']}%</td>
                <td style='color:{color}; font-weight:bold;'>{status}</td>
            </tr>
            """

        html = f"""
        <html>
        <body style="font-family: Arial;">

        <div style="text-align:center;">
            <h2 style="color:#003366;">TNM - Core Network</h2>
            <h3>PS Core Health Report</h3>
        </div>

        <p><strong>Generated By:</strong> Fraser Msusa</p>
        <p><strong>Generated On:</strong> {current_time}</p>

        <table border="1" cellpadding="8" cellspacing="0" width="100%">
            <tr style="background-color:#f2f2f2;">
                <th>NE Name</th>
                <th>Peak Traffic (MB)</th>
                <th>Peak Time</th>
                <th>Utilization %</th>
                <th>Health Status</th>
            </tr>
            {rows}
        </table>

        <br>
        <h3>Traffic Trend Charts</h3>

        <h4>1. PGW-U 2/3G Gi Traffic</h4>
        <img src="cid:chart_1"><br><br>

        <h4>2. 4G Data Traffic VDGW</h4>
        <img src="cid:chart_2"><br><br>

        <h4>3. PGW-U 2/3G Gn Peak Throughput</h4>
        <img src="cid:chart_3"><br><br>

        <h4>4. SGi Downlink Peak Throughput</h4>
        <img src="cid:chart_4"><br><br>

        <p style="font-size:12px; color:gray;">
        Automated report from Core Network Monitoring System.
        </p>

        </body>
        </html>
        """

        return html

    # Send Email
    def send_email(self, recipients, health_report, processor, attachment_path):

        msg = MIMEMultipart("related")
        msg["Subject"] = "TNM PS Core Health Report"
        msg["From"] = self.sender_email
        msg["To"] = ", ".join(recipients)

        # Generate charts
        chart_files = self.generate_charts(processor)

        # Attach HTML
        html_content = self.generate_html(health_report)
        msg.attach(MIMEText(html_content, "html"))

        # Attach charts inline
        for i, chart_path in enumerate(chart_files, start=1):
            with open(chart_path, "rb") as f:
                img = MIMEImage(f.read())
                img.add_header("Content-ID", f"<chart_{i}>")
                msg.attach(img)

        # Attach CSV
        with open(attachment_path, "rb") as f:
            part = MIMEBase("application", "octet-stream")
            part.set_payload(f.read())

        encoders.encode_base64(part)

        part.add_header(
            "Content-Disposition",
            f"attachment; filename={os.path.basename(attachment_path)}"
        )

        msg.attach(part)

        server = smtplib.SMTP_SSL("smtp.gmail.com", 465)
        server.login(self.sender_email, self.sender_password)

        server.sendmail(self.sender_email, recipients, msg.as_string())

        server.quit()

        print("Professional email with 4 charts sent successfully!")